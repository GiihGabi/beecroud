dezenas = [10, 20, 30, 40, 50, 70]
#insere em uma posição
dezenas.insert(0, 80)
print(dezenas)
#substitui o valor
dezenas[0] = 90
print(dezenas)
#Remove e mostra no fim
print(dezenas.pop())
#Remove e mostra em uma posição
print(dezenas.pop(0))
#lê a quantidade da variável
print(len(dezenas))
#ordenar a lista
print(dezenas.sort())
